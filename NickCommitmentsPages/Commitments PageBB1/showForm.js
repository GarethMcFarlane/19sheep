$('#newCommit').click(function()
{
	$('#form').show();
	$('#textCommit').show();
	//$('#duration').hide();

});

$('#cancel').click(function()
{
	$('#form').hide();
	//$('#cancel').hide();
	$('#textCommit').hide();
	$('#weight').hide();
	$('#heartRate').hide();
	$('#dream').hide();
});



$('#submit').click(function()
{
	$('#form').hide();
	//$('#cancel').hide();
	$('#textCommit').hide();
	$('#weight').hide();
	$('#heartRate').hide();
	$('#dream').hide();
	$('#duration').hide();	
});

		